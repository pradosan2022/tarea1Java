package ejercicios;

public class Ejercicio7 {
    public static void main(String[] args) {
        /*
        7. Área de un triángulo
        • Descripción: Dadas la base b = 5 y la altura h = 8 de un triángulo, calcula su área usando la fórmula:
        • Variables: b = 5, h = 8.
         */
        int b = 5;
        int h = 8;
        int area;
        area = (b * h)/2;
        System.out.println("Dada la base "+b+" y la altura "+h+" de un triángulo, su área es: "+area);
    }
}
